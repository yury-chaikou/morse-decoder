const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let char = expr.length / 10;
    let res = '';
    let mr = '';

    for (let i = 0; i < char; i++) {
        mr = expr.slice(i * 10, 10 + i * 10).replace(/11/g, "-").replace(/10/g, ".").replace(/0/g, "");
        if (mr === '**********') {
            res = res+' ';
        } else {
            res = res + MORSE_TABLE[mr];
        }
    }

    return res;
}

module.exports = {
    decode
}
